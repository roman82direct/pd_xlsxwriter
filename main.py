import pandas as pd


# Создаём несколько таблиц
employees = pd.DataFrame({
    'Имя': ['Анна', 'Борис', 'Виктор'],
    'Возраст': [25, 35, 40],
    'Отдел': ['Аналитика', 'Разработка', 'Тестирование'],
    'Зарплата': [80000, 120000, 60000]
})

departments = pd.DataFrame({
    'Отдел': ['Аналитика', 'Разработка', 'Тестирование'],
    'Этаж': [3, 5, 4]
})

salaries = pd.DataFrame({
    'Имя': ['Анна', 'Борис', 'Виктор'],
    'Зарплата': [80000, 120000, 60000]
})

# Сохраняем все на разные листы
with pd.ExcelWriter('отчёт.xlsx') as writer:
    employees.to_excel(writer, sheet_name='Сотрудники', index=False)
    departments.to_excel(writer, sheet_name='Отделы', index=False)
    salaries.to_excel(writer, sheet_name='Зарплаты', index=False)


#  все способы вставки колонок:

# Читаем созданный ранее файл
df = pd.read_excel('отчёт.xlsx')

print("Исходные данные:")
print(df)

# Способ 1: простое присваивание
df['Возраст через 10 лет'] = df['Возраст'] + 10

# Способ 2: колонка с одним значением
df['Категория'] = 'Сотрудник'

# Способ 3: вставка колонки в середину (после 'Имя')
df.insert(1, 'Пол', ['Ж', 'М', 'М'])

# Способ 4: колонка на основе условия
df['Возрастная группа'] = df['Возраст'].apply(
    lambda x: 'Молодой' if x < 30 else 'Средний'
)

print("После добавления колонок:")
print(df)

# Сохраняем в новый файл (чтобы не испортить оригинал)
df.to_excel('сотрудники_обновлённые.xlsx', index=False)

print("Файл сотрудники_обновлённые.xlsx успешно создан!")


