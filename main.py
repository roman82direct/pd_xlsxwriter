# Посмотрим на все способы добавления данных. Добавим новых людей разными способами:

# Читаем существующий файл
df = pd.read_excel('сотрудники.xlsx')

print("Было:")
print(df)

# Способ 1: создаём новую строку как DataFrame
новая_строка = pd.DataFrame([['Глеб', 28, 'Новосибирск']], columns=df.columns)

# Добавляем через concat
df = pd.concat([df, новая_строка], ignore_index=True)

print("После добавления Глеба:")
print(df)

# Способ 2: добавляем через loc (проще для одной строки)
df.loc[len(df)] = ['Дарья', 26, 'Екатеринбург']

print("После добавления Дарьи:")
print(df)

# Способ 3: добавляем несколько строк сразу
новые_сотрудники = pd.DataFrame([
    ['Игорь', 33, 'Самара'],
    ['Ольга', 29, 'Ростов']
], columns=df.columns)

df = pd.concat([df, новые_сотрудники], ignore_index=True)
