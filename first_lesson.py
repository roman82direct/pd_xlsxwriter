# Как подбирать ширину колонок.
# Обычно ширину подбирают «на глаз», но есть простые правила:
# Для коротких текстов (имена, города) — 15–20.
# Для длинных текстов (описания, адреса) — 30–50.
# Для чисел — 10–12.
# Для дат — 12–15.
# Лучше делать после записи данных.
# Обычно set_column вызывают после того, как все данные записаны, но это не принципиально.

with xlsxwriter.Workbook('ширина_диапазон.xlsx') as workbook:
    worksheet = workbook.add_worksheet()

    # Записываем заголовки
    headers = ['Имя', 'Фамилия', 'Отчество', 'Возраст', 'Город']
    worksheet.write_row('A1', headers)

    # Все колонки с текстом делаем шириной 15
    worksheet.set_column('A:C', 15)   # Имя, Фамилия, Отчество

    # Колонку с возрастом — уже
    worksheet.set_column('D:D', 8)    # Возраст

    # Город — пошире
    worksheet.set_column('E:E', 20)   # Город

